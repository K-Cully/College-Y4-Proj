namespace SticKart.Game
{
    /// <summary>
    /// An enumeration of the possible game states.
    /// </summary>
    public enum GameState
    {
        /// <summary>
        /// The menu is active.
        /// </summary>
        InMenu,

        /// <summary>
        /// The game is in play.
        /// </summary>
        InGame,

        /// <summary>
        /// The editor is active.
        /// </summary>
        InEditor
    }
}
