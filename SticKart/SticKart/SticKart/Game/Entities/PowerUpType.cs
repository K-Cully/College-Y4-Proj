namespace SticKart.Game.Entities
{
    /// <summary>
    /// An enumeration of power up types.
    /// </summary>
    public enum PowerUpType
    {
        /// <summary>
        /// No power up.
        /// </summary>
        None,

        /// <summary>
        /// An invincibility power up.
        /// </summary>
        Invincibility,

        /// <summary>
        /// A health power up.
        /// </summary>
        Health,

        /// <summary>
        /// A jump power up.
        /// </summary>
        Jump,

        /// <summary>
        /// An speed power up.
        /// </summary>
        Speed
    }
}
